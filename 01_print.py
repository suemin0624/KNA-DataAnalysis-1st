print("==========================")

# 여러 값 한 번에 출력하기
print("온도:", 75)  # 온도: 75 > 글자와 숫자 한 번에 출력, 쉼표는 띄어쓰기로 치환
print("온도():", 75, "도")
print("설비", 1, "번 상태:", "정상")

print("더하기" + "연산자를" + "사용하면" + "?")  # 더하기연산자를사용하면? 출력

# 더하기 연산자는 띄어쓰기 없이 출력
