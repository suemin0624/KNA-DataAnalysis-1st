# 글자와 숫자의 연산
print(123 + 456)  # 579 (숫자와 숫자를 더해서 출력)
print("123" + "456")  # 123456 (글자와 글자를 이어서 출력)

print(5 * 2)  # 10 (숫자와 숫자를 곱해서 출력)
print("5" * 2)  # 55 (글자를 * 뒤의 숫자만큼 반복해서 출력)

